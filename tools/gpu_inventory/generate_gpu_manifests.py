#!/usr/bin/env python3
"""Generates the machine-readable GPU runtime inventory manifests for design doc 0053.

Scans the git-tracked source tree and produces four JSON manifests under
tools/gpu_inventory/manifests/:

- gpu_operations.json: every file using the WebGPU wrapper (wgpu:: identifiers,
  wgpuXxx C entry points, WGPU* C types) plus the GPU operation methods it calls.
- shader_features.json: every WGSL shader (standalone .wgsl files and inline
  R"wgsl(...)" literals) with entry points, bindings, and language features.
- editor_integration.json: editor files that touch the GPU surface (wgpu types,
  the ImGui WebGPU backend, editor GPU feature defines).
- rust_dependencies.json: all tracked Rust sources and Cargo metadata, Rust build
  rule references, and Rust-built prebuilt archive declarations.

The scan is intentionally lexical: it over-approximates so that any new GPU
operation, shader feature, or Rust dependency edge changes at least one manifest.
Adding such a use without regenerating the manifests fails the `--check` step in
the Lint workflow.

Usage:
  python3 tools/gpu_inventory/generate_gpu_manifests.py            # regenerate
  python3 tools/gpu_inventory/generate_gpu_manifests.py --check    # verify up to date
"""

from __future__ import annotations

import argparse
import hashlib
import json
import re
import subprocess
import sys
from pathlib import Path

MANIFEST_DIR = Path(__file__).resolve().parent / "manifests"
REPO_ROOT = Path(__file__).resolve().parents[2]

GENERATOR_NOTE = (
    "Generated by tools/gpu_inventory/generate_gpu_manifests.py; do not edit by hand. "
    "Regenerate with: python3 tools/gpu_inventory/generate_gpu_manifests.py"
)

# WebGPU wrapper / C API token patterns. `wgpu::` identifiers come from the
# vendored webgpu.hpp wrapper, `wgpuXxx` are wgpu-native C entry points, and
# `WGPUXxx` are C ABI types.
WGPU_CPP_RE = re.compile(r"\bwgpu::([A-Za-z_][A-Za-z0-9_]*)")
WGPU_C_FUNC_RE = re.compile(r"\bwgpu[A-Z][A-Za-z0-9_]*\b")
WGPU_C_TYPE_RE = re.compile(r"\bWGPU[A-Z][A-Za-z0-9_]*\b")
IMGUI_WGPU_RE = re.compile(r"\bImGui_ImplWGPU_[A-Za-z0-9_]*\b")
EDITOR_WGPU_DEFINE_RE = re.compile(r"\bDONNER_EDITOR_WGPU\b")

# GPU operation methods, per the design 0053 current-state inventory. Matched as
# `.method(` inside files that already reference the WebGPU wrapper, so generic
# names like `draw` do not pick up unrelated call sites. This is a curated,
# labeled view; completeness of the ratchet does not depend on it because every
# scanned file also records a content hash, so any edit to a wgpu-using file
# forces a manifest regeneration. `RenderPassEncoder::end` is intentionally not
# tracked lexically (`.end(` is ambiguous with container `end()`); pass lifetime
# coverage comes from beginRenderPass/beginComputePass plus the content hash.
OPERATION_METHODS = (
    "beginComputePass",
    "beginRenderPass",
    "configure",
    "copyBufferToBuffer",
    "copyBufferToTexture",
    "copyTextureToBuffer",
    "copyTextureToTexture",
    "createBindGroup",
    "createBindGroupLayout",
    "createBuffer",
    "createCommandEncoder",
    "createComputePipeline",
    "createPipelineLayout",
    "createRenderPipeline",
    "createSampler",
    "createShaderModule",
    "createTexture",
    "createView",
    "destroy",
    "dispatchWorkgroups",
    "draw",
    "drawIndexed",
    "finish",
    "getCapabilities",
    "getConstMappedRange",
    "getCurrentTexture",
    "getQueue",
    "hasFeature",
    "mapAsync",
    "onSubmittedWorkDone",
    "poll",
    "present",
    "release",
    "requestAdapter",
    "requestDevice",
    "setBindGroup",
    "setIndexBuffer",
    "setPipeline",
    "setScissorRect",
    "setVertexBuffer",
    "setViewport",
    "submit",
    "unmap",
    "writeBuffer",
    "writeTexture",
)
OPERATION_RE = re.compile(r"\.\s*(" + "|".join(OPERATION_METHODS) + r")\s*\(")

# WGSL parsing patterns. Entry-point and binding attributes may appear in any
# order, so both patterns match an attribute blob and the fields are extracted
# from it afterwards.
WGSL_ENTRY_RE = re.compile(
    r"((?:@(?:vertex|fragment|compute)|@workgroup_size\([^)]*\))"
    r"(?:\s*(?:@(?:vertex|fragment|compute)|@workgroup_size\([^)]*\)))*)"
    r"\s*fn\s+([A-Za-z_][A-Za-z0-9_]*)",
    re.DOTALL,
)
WGSL_ENTRY_STAGE_RE = re.compile(r"@(vertex|fragment|compute)\b")
WGSL_WORKGROUP_SIZE_RE = re.compile(r"@workgroup_size\(([^)]*)\)")
WGSL_BINDING_RE = re.compile(
    r"((?:@group\(\d+\)|@binding\(\d+\))\s*(?:@group\(\d+\)|@binding\(\d+\))\s*)"
    r"var\s*(<[^>]*>)?\s*([A-Za-z_][A-Za-z0-9_]*)\s*:\s*([^;]+);"
)
WGSL_GROUP_RE = re.compile(r"@group\((\d+)\)")
WGSL_BINDING_INDEX_RE = re.compile(r"@binding\((\d+)\)")
WGSL_BUILTIN_RE = re.compile(r"@builtin\(([A-Za-z_][A-Za-z0-9_]*)\)")
WGSL_STRUCT_RE = re.compile(r"\bstruct\s+([A-Za-z_][A-Za-z0-9_]*)")
# Inline WGSL raw strings, with or without an `identifier =` assignment prefix;
# unassigned literals (e.g. passed directly as an argument) get an index-based
# manifest key.
INLINE_WGSL_RE = re.compile(
    r"(?:([A-Za-z_][A-Za-z0-9_]*)\s*=\s*)?R\"wgsl\((.*?)\)wgsl\"", re.DOTALL
)

